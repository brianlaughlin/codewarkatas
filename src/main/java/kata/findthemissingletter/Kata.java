package kata.findthemissingletter;

public class Kata {
    public static char findMissingLetterMain(char[] array)
    {
        return ' ';
    }

    public static int findMissingLetter(char[] array)
    {
        int charValue = 0;
        for (int i = 0; i < array.length; i++){

            charValue = array[i];
        }
        return charValue;
    }
}
